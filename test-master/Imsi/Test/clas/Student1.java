package clas;

public class Student1 {//학생의 이름을 반환하는 메서드를 만드시오5
	String address;//문자형 스트링 변수로 주소 대입
	int age;// 정수형변수 인트에 이름지정
	public String getaddress() {// 주소를 호출하는 메서드
		return address;
	}
	public int getage() {// 나이를 호출하는 메서드
		return age;
	}
	public void setage(int age) {//학생의 이름을 멤버 변수에 대입하는 메서드 
		age = 17;
	}


}
